SELECT
  CHILD_NUMBER,
  EXECUTIONS,
  ROWS_PROCESSED,
  BUFFER_GETS,
  BIND_SET_HASH_VALUE
FROM
  V$SQL_CS_STATISTICS
WHERE
  SQL_ID='5f2624a1nuchc'
ORDER BY
  CHILD_NUMBER;


SELECT
  CHILD_NUMBER,
  PREDICATE,
  RANGE_ID,
  LOW,
  HIGH
FROM
  V$SQL_CS_SELECTIVITY
WHERE
  SQL_ID='5f2624a1nuchc'
ORDER BY
  CHILD_NUMBER, PREDICATE DESC;
